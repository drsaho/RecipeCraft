const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const FavoriteRecipeSchema = new Schema({
    recipeId: {
        type: String,
        required: true
    },
    title: {
        type: String,
        required: true
    },
    image: {
        type: String,
        required: true
    },
    user: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
});

module.exports = mongoose.model('FavoriteRecipe', FavoriteRecipeSchema);
